// Classe pública
public class Empregado {

// Declaração de atributos
  String id;

  String nome;

  int idade;

  String cargo;

  double salario;

// Construtor da classe que é usado para inicializar os atributos
  Empregado (String id, String nome, int idade, String cargo, double salario) {

// Parâmetros que inicializam os atributos
    this.id = id;
    
    this.nome = nome;
    
    this.idade = idade;
    
    this.cargo = cargo;
    
    this.salario = salario;
    
  }
      
}
 
